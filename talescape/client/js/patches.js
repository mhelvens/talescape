define(['patches'], function () { //////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


	//// String format: "{{1}} {{2}}".format("a", "b") == "a b"
	//
	if (String.prototype.format) {
		console.error("String.prototype.format was already defined!");
	} else {
		String.prototype.format = function () {
			var args = arguments;
			return this.replace(/{{(\d+)}}/g, function (match, number) {
				return (number > 0 && typeof args[number - 1] != 'undefined')
						? args[number - 1] : match;
			});
		};
	}


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
});/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
